const size_t N_BYTES = 4;
const size_t N_ROUNDS = 10;

void key_expansion(uint32_t key[N_BYTES], uint32_t w[N_BYTES * (N_ROUNDS + 1)]) {
  for (size_t i = 0; i <= 3; i++) {
    w[i] = key[i];
  }

  for (size_t i = 4; i <= 43; i++) {
    uint32_t temp = w[i - 1];
    if (i % 4 == 0) {
      // sub_word(rot_word(temp)) ^ rcon[i / 4]
      temp = (S_LUT[(temp & 0xff000000) >> 24] << 16) | (S_LUT[(temp & 0x00ff0000) >> 16] << 8) |
             S_LUT[(temp & 0x0000ff00) >> 8] | (S_LUT[(temp & 0x000000ff)] << 24);
      temp ^= RCON[i / 4 - 1];
    }
    w[i] = w[i - 4] ^ temp;
  }
}

void aes_encrypt(
  uint32_t in[N_BYTES],
  uint32_t out[N_BYTES],
  uint32_t w[N_BYTES * (N_ROUNDS + 1)]
) {
  uint32_t state[2][N_BYTES];

  // AddRoundKey(state, w[0, Nb - 1])
  for (size_t i = 0; i < N_BYTES; i++) {
    state[0][i] = in[i] ^ w[i];
  }

  uint8_t b0, b1, b2, b3;

  for (size_t i = 1; i < N_ROUNDS; i++) {
    size_t prev = (i - 1) % 2;
    size_t curr = i % 2;

    // for every column, calculate the new column after subbytes, shiftrows, mixcolumns and
    // addroundkey
    for (size_t j = 0; j < N_BYTES; j++) {
      size_t b0_offset = j;
      size_t b1_offset = (j + 1) % N_BYTES;
      size_t b2_offset = (j + 2) % N_BYTES;
      size_t b3_offset = (j + 3) % N_BYTES;

      // 2 3 1 1
      b0 = LUT[1][state[prev][b0_offset] & 0xff] ^ LUT[2][(state[prev][b1_offset] >> 8) & 0xff] ^
           LUT[0][(state[prev][b2_offset] >> 16) & 0xff] ^
           LUT[0][(state[prev][b3_offset] >> 24) & 0xff];
      // 1 2 3 1
      b1 = LUT[0][state[prev][b0_offset] & 0xff] ^ LUT[1][(state[prev][b1_offset] >> 8) & 0xff] ^
           LUT[2][(state[prev][b2_offset] >> 16) & 0xff] ^
           LUT[0][(state[prev][b3_offset] >> 24) & 0xff];
      // 1 1 2 3
      b2 = LUT[0][state[prev][b0_offset] & 0xff] ^ LUT[0][(state[prev][b1_offset] >> 8) & 0xff] ^
           LUT[1][(state[prev][b2_offset] >> 16) & 0xff] ^
           LUT[2][(state[prev][b3_offset] >> 24) & 0xff];
      // 3 1 1 2
      b3 = LUT[2][state[prev][b0_offset] & 0xff] ^ LUT[0][(state[prev][b1_offset] >> 8) & 0xff] ^
           LUT[0][(state[prev][b2_offset] >> 16) & 0xff] ^
           LUT[1][(state[prev][b3_offset] >> 24) & 0xff];

      state[curr][j] = (b3 << 24) | (b2 << 16) | (b1 << 8) | b0;
      state[curr][j] ^= w[i * N_BYTES + j];
    }
  }

  // last round without mixcolumns
  for (size_t j = 0; j < N_BYTES; j++) {
    size_t b0_offset = j;
    size_t b1_offset = (j + 1) % N_BYTES;
    size_t b2_offset = (j + 2) % N_BYTES;
    size_t b3_offset = (j + 3) % N_BYTES;

    b0 = S_LUT[state[(N_ROUNDS - 1) % 2][b0_offset] & 0xff];
    b1 = S_LUT[(state[(N_ROUNDS - 1) % 2][b1_offset] >> 8) & 0xff];
    b2 = S_LUT[(state[(N_ROUNDS - 1) % 2][b2_offset] >> 16) & 0xff];
    b3 = S_LUT[(state[(N_ROUNDS - 1) % 2][b3_offset] >> 24) & 0xff];

    state[N_ROUNDS % 2][j] = (b3 << 24) | (b2 << 16) | (b1 << 8) | b0;
    state[N_ROUNDS % 2][j] ^= w[N_ROUNDS * N_BYTES + j];
  }

  memcpy(out, state[N_ROUNDS % 2], N_BYTES * sizeof(uint32_t));
}

int main(int argc, char* argv[]) {
  uint32_t key[N_BYTES];
  uint32_t w[N_BYTES * (N_ROUNDS + 1)];
  uint32_t plaintext[N_BYTES];
  uint32_t ciphertext[N_BYTES];

  // read key
  
  char str[33];
  uint8_t byte;
  scanf("%s", str);

  for (size_t i = 0; i < N_BYTES; i++) {
    key[i] = 0;
    for (size_t j = 0; j < 4; j++) {
      sscanf(str + i * 8 + j * 2, "%02hhx", &byte);
      key[i] = key[i] | (byte << (j << 3));
    }
  }

  // read plaintext
  scanf("%s", str);

  for (size_t i = 0; i < N_BYTES; i++) {
    plaintext[i] = 0;
    for (size_t j = 0; j < 4; j++) {
      sscanf(str + i * 8 + j * 2, "%02hhx", &byte);
      plaintext[i] = plaintext[i] | (byte << (j << 3));
    }
  }

  key_expansion(key, w);
  aes_encrypt(plaintext, ciphertext, w);

  for (size_t i = 0; i < N_BYTES; i++) {
    printf("%02X", ciphertext[i] & 0xff);
    printf("%02X", (ciphertext[i] >> 8) & 0xff);
    printf("%02X", (ciphertext[i] >> 16) & 0xff);
    printf("%02X", (ciphertext[i] >> 24) & 0xff);
  }

  return 0;
}